function suma(num1, num2) {
    const result = num1 + num2;
    return result;
}

function rest(num1, num2) {
    const result = num1 - num2;
    return result;
}
function mult(num1, num2) {
    const result = num1 * num2;
    return result;
}
function div(num1, num2) {
    const result = num1 / num2;
    return result;
}

export {suma, rest, mult, div}
